package com.dominik.controllers;

import java.io.Serializable;


public class Table{

	String name;
	
	String surname;
	
	public Table() {}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public void setSurname(String surname) {
		this.surname = surname;
	}
	
	public String getName() {
		return name;
	}
	
	public String getSurname() {
		return surname;
	}
	
	

}
